package com.polymorphism;

public class Parent {
	public void show() {
		System.out.println("Parent's show() method: ");
	}
	
	public int draw(int n) {
		return n;
	}
	
	void m1() {
		System.out.println("From parent's m1() method: ");
	}
	
	void m2() {
		System.out.println("From parent's m2() method: ");
	}

}
